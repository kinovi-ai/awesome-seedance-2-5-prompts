function slugify(text) {
  return text
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

function saveCache() {
  writeFileSync(cachePath, JSON.stringify(cache, null, 2) + '\n');
}

function protectPlaceholders(text) {
  const tokens = [];
  const protectedText = text.replace(/<<<[^>]+>>>/g, (match) => {
    const token = `ZXPLACEHOLDER${tokens.length}ZX`;
    tokens.push([token, match]);
    return token;
  });
  return { protectedText, tokens };
}

function restorePlaceholders(text, tokens) {
  let restored = text;
  for (const [token, value] of tokens) {
    restored = restored.replaceAll(token, value);
    restored = restored.replaceAll(token.toLowerCase(), value);
  }
  return restored;
}

async function translateText(text, target, source = 'en') {
  if (!text || target === source) return text;
  const key = `${source}|${target}|${text}`;
  if (cache[key]) return cache[key];

  const { protectedText, tokens } = protectPlaceholders(text);
  if (translateDelayMs > 0) {
    await new Promise((resolveDelay) => setTimeout(resolveDelay, translateDelayMs));
  }
  const params = new URLSearchParams({
    client: 'gtx',
    sl: source,
    tl: target,
    dt: 't',
    q: protectedText,
  });
  const data = await postJsonWithRetry(params);
  const translated = restorePlaceholders((data[0] || []).map((part) => part[0]).join(''), tokens);
  cache[key] = translated;
  saveCache();
  return translated;
}

async function postJsonWithRetry(params, attempts = 5) {
  let lastError;
  for (let attempt = 1; attempt <= attempts; attempt += 1) {
    try {
      return await postJson(params);
    } catch (error) {
      lastError = error;
      const delay = 500 * attempt;
      await new Promise((resolveDelay) => setTimeout(resolveDelay, delay));
    }
  }
  throw lastError;
}

function postJson(params) {
  return new Promise((resolveJson, reject) => {
    const body = params.toString();
    const req = request({
      hostname: 'translate.googleapis.com',
      path: '/translate_a/single',
      method: 'POST',
      headers: {
        'Content-Type': 'application/x-www-form-urlencoded;charset=UTF-8',
        'Content-Length': Buffer.byteLength(body),
        'User-Agent': 'awesome-seedance-2-5-prompts/1.0',
      },
    }, (response) => {
      let body = '';
      response.setEncoding('utf8');
      response.on('data', (chunk) => {
        body += chunk;
      });
      response.on('end', () => {
        if (response.statusCode < 200 || response.statusCode >= 300) {
          reject(new Error(`Translate failed ${response.statusCode}: ${body.slice(0, 200)}`));
          return;
        }
        try {
          resolveJson(JSON.parse(body));
        } catch (error) {
          reject(error);
        }
      });
    });
    req.on('error', reject);
    req.write(body);
    req.end();
  });
}

async function translateLong(text, lang) {
  if (lang.code === 'en') return text;
  if (lang.code === 'zh') return text;
  const source = lang.code === 'zh-TW' ? 'zh-CN' : 'en';
  const chunks = chunkText(text, 4500);
  const translated = [];
  for (const chunk of chunks) {
    translated.push(chunk.trim() ? await translateText(chunk, lang.google, source) : chunk);
  }
  return translated.join('');
}

function chunkText(text, maxLength) {
  if (text.length <= maxLength) return [text];
  const lines = text.split(/(\n)/);
  const chunks = [];
  let current = '';
  for (const line of lines) {
    if (current && current.length + line.length > maxLength) {
      chunks.push(current);
      current = '';
    }
    current += line;
  }
  if (current) chunks.push(current);
  return chunks;
}

async function localizeStrings(lang) {
  if (lang.code === 'en') return enStrings;
  if (lang.code === 'zh') return zhStrings;
  if (lang.code === 'zh-TW') {
    return Object.fromEntries(Object.entries(zhStrings).map(([key, value]) => [key, toTraditional(value)]));
  }
  const base = lang.code === 'zh-TW' ? zhStrings : enStrings;
  const source = lang.code === 'zh-TW' ? 'zh-CN' : 'en';
  const entries = await Promise.all(Object.entries(base).map(async ([key, value]) => [key, await translateText(value, lang.google, source)]));
  return Object.fromEntries(entries);
}

